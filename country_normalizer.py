"""
country_normalizer.py
Parses raw Greek country adjective strings from athinorama.gr into
canonical, comma-separated Greek country names.

Usage:
    from country_normalizer import parse_country, normalize_country_field

    parse_country("Αγγλο-γαλλική")          # → ["Βρετανία", "Γαλλία"]
    normalize_country_field("Αγγλογαλλική") # → "Βρετανία, Γαλλία"
"""

import re
import unicodedata


def _strip_accents(s: str) -> str:
    """Return lowercase s with Greek diacritics removed (for fuzzy matching)."""
    return ''.join(
        c for c in unicodedata.normalize('NFD', s.lower())
        if unicodedata.category(c) != 'Mn'
    )


# Greek base letters → ASCII for cross-encoding comparison (ο U+03BF vs o U+006F etc.)
_GREEK_ASCII = str.maketrans(
    'αβγδεζηθικλμνξοπρστυφχω',
    'abgdezh-iklmnxoprstufho',
)


def _to_key(s: str) -> str:
    """
    Normalise a string for prefix/suffix comparison:
    strip accents, map Greek base letters to ASCII.
    This ensures Greek ο (U+03BF) and Latin o (U+006F) compare equal,
    and removes encoding issues from hand-typed prefix tables.
    """
    stripped = _strip_accents(s)
    return stripped.translate(_GREEK_ASCII)


