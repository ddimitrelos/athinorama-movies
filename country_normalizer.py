# Pre-compute normalised keys for fast prefix matching
_PREFIXES_KEYED = [(
    _to_key(prefix), len(_to_key(prefix)), canonical
) for prefix, canonical in _PREFIXES]
# Re-sort by key length (longest first) — _to_key may shrink '-' to itself
_PREFIXES_KEYED.sort(key=lambda x: x[1], reverse=True)

# Build a normalised-key lookup for CANONICAL_MAP
_CANONICAL_KEYED: dict[str, str] = {
    _to_key(k): v for k, v in CANONICAL_MAP.items()
}


_NON_COUNTRIES: frozenset[str] = frozenset({'ερωτική', 'ερωτικη'})


def _lookup(token: str) -> str | None:
    """Look up a single token in CANONICAL_MAP, returns canonical or None."""
    t = token.strip().lower()
    if t in _NON_COUNTRIES:
        return ''   # known non-country; caller treats '' as "no match, skip"
    # Exact lower match first
    if t in CANONICAL_MAP:
        return CANONICAL_MAP[t]
    # Normalised key lookup (handles accent variants and Greek/Latin ο/o)
    key = _to_key(t)
    hit = _CANONICAL_KEYED.get(key)
    if hit is not None:
        return hit
    # Strip trailing period for abbreviated forms like "Ιταλ.", "Γιουγκ.", "αμερικάν."
    if t.endswith('.') and len(t) > 1:
        t2 = t.rstrip('.')
        if t2 in CANONICAL_MAP:
            return CANONICAL_MAP[t2]
        return _CANONICAL_KEYED.get(_to_key(t2))
    return None


def _parse_non_hyphenated(s: str) -> list[str] | None:
    """
    Try to decompose s into known country parts using greedy prefix matching.
    Returns list of canonical names, or None if no match.
    """
    s_key = _to_key(s)
    # Build a parallel list of positions in original s corresponding to s_key
    # Since _to_key maps each char to exactly 1 char EXCEPT '-' which maps to '-'
    # (see _GREEK_ASCII — missing chars map to themselves), positions align 1:1.
    results: list[str] = []
    pos = 0
    while pos < len(s_key):
        matched = False
        for prefix_key, prefix_len, canonical in _PREFIXES_KEYED:
            if s_key[pos:].startswith(prefix_key):
                results.append(canonical)
                pos += prefix_len
                matched = True
                break
        if not matched:
            # Remaining string: try direct CANONICAL_MAP lookup (full suffix)
            # pos aligns with s_key, which aligns 1:1 with s (same char count)
            remainder = s[pos:]
            hit = _lookup(remainder)
            if not hit and pos > 0:
                # In Greek compounds the connecting ο can be shared between
                # the previous prefix (ends in ο) and the suffix (starts with ο).
                # Try prepending ο to recover the full suffix form.
                hit = _lookup('ο' + remainder)
            if hit:
                results.append(hit)
                pos = len(s_key)
            else:
                # Cannot parse — return None to signal failure
                return None
    return results if results else None


def parse_country(raw: str | None) -> list[str]:
    """
    Parse a raw country string from the DB into a list of canonical country names.

    Handles:
    - None / empty → []
    - Direct lookup → [canonical]
    - Comma / " και " / " and " separated → split + recurse
    - Hyphen-separated compounds → split + per-part lookup
    - Slash-abbreviated → split + per-part lookup
    - Non-hyphenated compounds → greedy prefix matching
    - Already normalised values → idempotent
    """
    if not raw:
        return []
    raw = raw.strip()
    if not raw:
        return []

    # --- 1. Direct lookup ---
    direct = _lookup(raw)
    if direct is not None:
        if direct == '':
            return []   # known non-country (e.g. genre misclassified as country)
        # Direct could be a pre-built compound like "Μογγολία, Κίνα"
        if ',' in direct:
            return [c.strip() for c in direct.split(',')]
        return [direct]

    # --- 2. Handle parenthetical suffixes like "Κινέζικη (Χονγκ-Κονγκ)" ---
    paren_match = re.match(r'^([^(]+)\s*\(([^)]+)\)\s*$', raw)
    if paren_match:
        hit = _lookup(raw.lower())
        if hit:
            return [hit] if ',' not in hit else [c.strip() for c in hit.split(',')]
        # Try each part
        parts = [paren_match.group(1).strip(), paren_match.group(2).strip()]
        result = []
        for p in parts:
            sub = parse_country(p)
            for c in sub:
                if c not in result:
                    result.append(c)
        if result:
            return result

    # --- 3. Comma / " και " / " and " separation ---
    # Split on comma, " και ", " and ", or " - " (with spaces) if they contain full words
    for sep in [', ', ',', ' και ', ' and ']:
        if sep in raw:
            parts = [p.strip() for p in raw.split(sep) if p.strip()]
            # Only use this split if we get ≥2 non-empty parts
            if len(parts) >= 2:
                result = []
                for p in parts:
                    for c in parse_country(p):
                        if c not in result:
                            result.append(c)
                if result:
                    return result

    # --- 4. Slash-separated abbreviations ---
    if '/' in raw:
        parts = [p.strip() for p in raw.split('/') if p.strip()]
        if len(parts) >= 2:
            result = []
            for p in parts:
                sub = parse_country(p)
                if not sub:
                    # Maybe it's an abbreviated suffix like "αμερικάν." — try with common endings
                    for suffix in ['ική', 'ικη', 'η']:
                        sub = parse_country(p.rstrip('.') + suffix)
                        if sub:
                            break
                for c in sub:
                    if c not in result:
                        result.append(c)
            if result:
                return result

    # --- 5. Dot-separated (like "Γιουγκ.,Ιταλική" or "Ιταλ.,Γερμανική") ---
    if '.,  ' in raw or '.,' in raw:
        parts = [p.strip().rstrip('.') for p in re.split(r'[.,]+', raw) if p.strip().rstrip('.')]
        result = []
        for p in parts:
            for c in parse_country(p):
                if c not in result:
                    result.append(c)
        if result:
            return result

    # --- 6. Hyphen-separated compound adjectives ---
    if '-' in raw:
        parts = [p.strip() for p in raw.split('-') if p.strip()]
        if len(parts) >= 2:
            result = []
            for p in parts:
                hit = _lookup(p)
                if hit:
                    if hit not in result:
                        result.append(hit)
                else:
                    # try prefix lookup (e.g. "Αγγλo" is both prefix and listed)
                    hit2 = _lookup(p + 'ο')  # add ο to make it a known prefix form
                    if hit2 and hit2 not in result:
                        result.append(hit2)
                    # otherwise we might have a capitalized prefix form
            if result:
                return result

    # --- 7. Non-hyphenated compound (greedy prefix matching) ---
    parts_nh = _parse_non_hyphenated(raw)
    if parts_nh:
        seen: list[str] = []
        for c in parts_nh:
            if c not in seen:
                seen.append(c)
        return seen

    # --- 8. Fallback: return raw as-is (don't corrupt unknown values) ---
    return [raw]


def normalize_country_field(raw: str | None) -> str:
    """
    Convert raw country string to canonical comma-separated string.
    Returns "" if input is empty/None.
    Returns the original value if it cannot be parsed.
    """
    if not raw or not raw.strip():
        return ''
    result = parse_country(raw)
    return ', '.join(result)
